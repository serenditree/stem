#!/usr/bin/env bash
########################################################################################################################
# TERRA-SCALE
########################################################################################################################
_SERVICE=terra-scale
_ORDINAL=5

_IMAGE=-
_TAG=-

if [[ " $* " =~ " info " ]] || [[ -n "$_ARG_DRYRUN" ]]; then
    echo "${_ORDINAL} ${_SERVICE} ${_IMAGE} ${_TAG} $(realpath $0)"
fi
########################################################################################################################
# UP
########################################################################################################################
if [[ " $* " =~ " up " ]]; then
    sc_heading 1 "Setting up ${_SERVICE}"
########################################################################################################################
# DOWN
########################################################################################################################
elif [[ " $* " =~ " down " ]]; then
    sc_heading 1 "Deleting ${_SERVICE}"
fi
